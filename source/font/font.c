static void set_pixel(u32_t x, u32_t y, u32_t c)
{
	s5pv210_screen_surface()->maps.point(s5pv210_screen_surface(), x, y, c, BLEND_MODE_REPLACE);
}

static void lcd_char(u32_t x, u32_t y, u32_t fc, u32_t bc, u8_t ch)
{
	u8_t * p;
	u32_t i, j;

	if(ch > 0x7f)
		return;

	p = (u8_t *)(&font_ascii_16x8[ch][0]);

	for(j = 0; j < 16; j++)
	{
		for(i = 0; i < 8; i++)
		{
			if(*p & (0x80 >> i))
				set_pixel(x + i, y + j, fc);
			else
				set_pixel(x + i, y + j, bc);
		}
		p++;
	}
}

static void lcd_textout(u32_t x, u32_t y, u32_t fc, u32_t bc, u8_t * buf, u32_t cnt)
{
	u16_t ox = x;
	u32_t i;
	for(i = 0; i < cnt; i++)
	{
		lcd_char(ox, y, fc, bc, buf[i]);
		ox += 8;
	}
}

void lcd_print(u32_t x, u32_t y, u32_t fc, u32_t bc, const char * fmt, ...)
{
	va_list ap;
	char buf[1024];
	int len;

	va_start(ap, fmt);
	len = vsnprintf(buf, sizeof(buf), fmt, ap);
	va_end(ap);

	lcd_textout(x, y, fc, bc, (u8_t *)buf, len);
}
